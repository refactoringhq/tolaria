const MESSAGES: Record<UiLocale, Record<string, string>> = {
  en: {},
  'zh-CN': ZH_CN_MESSAGES,
}

const listeners = new Set<() => void>()

function detectNavigatorLocale(): UiLocale {
  if (typeof navigator === 'undefined') return DEFAULT_UI_LOCALE
  return navigator.language.toLowerCase().startsWith('zh') ? 'zh-CN' : DEFAULT_UI_LOCALE
}

function readStoredLocale(): UiLocale | null {
  if (typeof window === 'undefined') return null
  return normalizeUiLocale(window.localStorage.getItem(UI_LANGUAGE_STORAGE_KEY))
}

let currentLocale: UiLocale = readStoredLocale() ?? detectNavigatorLocale()

function notifyLocaleChange() {
  for (const listener of listeners) listener()
}

export function normalizeUiLocale(value: string | null | undefined): UiLocale | null {
  if (!value) return null
  const normalized = value.trim()
  if (normalized === 'en' || normalized === 'zh-CN') return normalized
  if (normalized.toLowerCase() === 'zh' || normalized.toLowerCase() === 'zh-cn') return 'zh-CN'
  return null
}

function interpolate(template: string, params: TranslationParams | undefined): string {
  if (!params) return template
  return template.replace(/\{(\w+)\}/g, (_, key: string) => {
    const value = params[key]
    return value === null || value === undefined ? '' : String(value)
  })
}

export function translate(message: string, params?: TranslationParams, locale: UiLocale = currentLocale): string {
  const template = MESSAGES[locale][message] ?? message
  return interpolate(template, params)
}

export function t(message: string, params?: TranslationParams): string {
  return translate(message, params, currentLocale)
}

export function getUiLocale(): UiLocale {
  return currentLocale
}

export function setUiLocale(locale: UiLocale): void {
  if (locale === currentLocale) return
  currentLocale = locale
  if (typeof window !== 'undefined') {
    window.localStorage.setItem(UI_LANGUAGE_STORAGE_KEY, locale)
    document.documentElement.lang = locale
  }
  notifyLocaleChange()
}

export function useI18n() {
  const locale = useSyncExternalStore(
    (listener) => {
      listeners.add(listener)
      return () => listeners.delete(listener)
    },
    getUiLocale,
    getUiLocale,
  )

  const boundTranslate = useCallback(
    (message: string, params?: TranslationParams) => translate(message, params, locale),
    [locale],
  )

  return {
    locale,
    setLocale: setUiLocale,
    t: boundTranslate,
  }
}

export function useUiLocaleSync(preferredLocale: UiLocale | null | undefined) {
  useEffect(() => {
    const nextLocale = preferredLocale ?? readStoredLocale() ?? detectNavigatorLocale()
    if (typeof document !== 'undefined') document.documentElement.lang = nextLocale
    setUiLocale(nextLocale)
  }, [preferredLocale])
}

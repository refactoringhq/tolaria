export type ThemeId = (typeof APP_THEMES)[number]['id']

export const DEFAULT_THEME_ID: ThemeId = 'graphiteSapphire'

const APP_THEME_MAP = new Map(APP_THEMES.map((theme) => [theme.id, theme]))

export function isThemeId(value: unknown): value is ThemeId {
  return typeof value === 'string' && APP_THEME_MAP.has(value)
}

export function getThemeById(themeId: ThemeId): AppTheme {
  return APP_THEME_MAP.get(themeId) ?? APP_THEME_MAP.get(DEFAULT_THEME_ID)!
}

export function getThemeSelectOptions() {
  return APP_THEMES.map((theme) => ({
    value: theme.id,
    label: theme.name,
  }))
}

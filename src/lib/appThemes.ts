export type ThemeMaterial = 'ultraThinMaterial' | 'thinMaterial' | 'regularMaterial' | 'bar'
export type ThemeAppearance = 'light' | 'dark'

export interface ThemeTokens {
  appBackground: string
  backgroundGradient: [string, string, string]
  ambientBlobColors: [string, string, string]
  glassMaterial: ThemeMaterial
  glassFill: string
  glassFillStrong: string
  glassStroke: string
  glassHighlight: string
  glassShadow: string
  textPrimary: string
  textSecondary: string
  textMuted: string
  textDisabled: string
  accentPrimary: string
  accentSecondary: string
  success: string
  warning: string
  danger: string
  selection: string
  focusRing: string
  buttonGradient: [string, string]
  fontDisplay: string
  fontBody: string
  fontCaption: string
  fontMono: string
  cornerRadiusCard: number
  cornerRadiusButton: number
  cornerRadiusPanel: number
}

export interface AppTheme {
  id: string
  name: string
  mood: string
  appearance: ThemeAppearance
  tokens: ThemeTokens
}

